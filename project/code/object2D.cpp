//=====================================================
// 優先順位を決めるコンストラクタ
//=====================================================
CObject2D::CObject2D(int nPriority) : CGameObject(nPriority)
{

}

//=====================================================
// デストラクタ
//=====================================================
CObject2D::~CObject2D()
{

}

//=====================================================
// 初期化
//=====================================================
HRESULT CObject2D::Init(void)
{
	CGameObject::Init();

	// 2D描画に追加
	Add2D();

	return S_OK;
}

//=====================================================
// 終了処理
//=====================================================
void CObject2D::Uninit(void)
{
	Remove2D();

	CGameObject::Uninit();
}

//=====================================================
// 更新処理
//=====================================================
void CObject2D::Update(void)
{
	CGameObject::Update();
}

//=====================================================
// 描画
//=====================================================
void CObject2D::Draw(void)
{
	CGameObject::Draw();
}